import { Injectable } from '@angular/core';

@Injectable()
export class UtilityService {

    // TODO: Replace with something else? 
    generateUniqueId() {
        function s4() {
            return Math.floor((1 + Math.random()) * 0x10000)
                .toString(16)
                .substring(1);
        }
        return s4() + s4() + '-' + s4() + '-' + s4() + '-' +
            s4() + '-' + s4() + s4() + s4();
    }

}
